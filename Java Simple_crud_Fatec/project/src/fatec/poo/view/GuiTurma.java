    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int x;
        for (x = 0; x < cadastroT.size(); x++){
            if (cadastroT.get(x).getSiglaTurma().equals(txtSigla.getText())){
                break;
            }
        }
        
        if (x < cadastroT.size()){
            posTurma = x; 
        }else{
            posTurma = -1;
        }
        
        if (posTurma >= 0){
            txtSigla.setText(cadastroT.get(posTurma).getSiglaTurma());
            txtNome.setText(cadastroT.get(posTurma).getDescricao());
            txtQtdeVagas.setText(String.valueOf(cadastroT.get(posTurma).getQtdVagas()));
            txtDataInicio.setText(cadastroT.get(posTurma).getDataInicio());
            txtDataTermino.setText(cadastroT.get(posTurma).getDataTermino());
            
            btnConsultar.setEnabled(false);
            btnInserir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }else{
            btnConsultar.setEnabled(false);
            btnInserir.setEnabled(true);
            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            txtSigla.requestFocus();
        }
        
        cboxCurso.setEnabled(false);
        txtSigla.setEnabled(false);
        txtNome.setEnabled(true);
        txtQtdeVagas.setEnabled(true);
        txtDataInicio.setEnabled(true);
        txtDataTermino.setEnabled(true);
        cboxPeriodo.setEnabled(true);
        txtNome.requestFocus();
        
    }//GEN-LAST:event_btnConsultarActionPerformed

