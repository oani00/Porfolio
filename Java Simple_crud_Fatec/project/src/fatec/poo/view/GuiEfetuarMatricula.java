    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
            
            double valorV = Double.parseDouble(txtValor.getText());
            double valorP = Double.parseDouble(txtValor.getText());
            String dataMa = txtDataMatricu.getText();
            dataMa = dataMa.replaceAll("[^\\d]", "");
            
            
            Matricula matricula = new Matricula(txtNome.getText());
            matricula.setData(dataMa);
            
            if(rbtnParcelado.isSelected()){
               AVista avista = new AVista();
               
               avista.setAgencia(Integer.parseInt(txtAgencia.getText()));
               avista.setnCheque(Integer.parseInt(txtCheque.getText()));
               avista.setPreData(txtDataPagto.getText());
               
               avista.setValor(valorP);
                
              
            }
            else if(rbtnVista.isSelected()){
                APrazo aprazo = new APrazo();
                
                aprazo.setQtdeMensalidade(Integer.parseInt(txtMensalidade.getText()));
                aprazo.setTaxaJuros(Double.parseDouble(txtJuros.getText()));
                aprazo.setDtVencimento(txtDataVenci.getText());
                
                valorV = valorV/Integer.parseInt(txtMensalidade.getText());
                valorV = valorV+(valorV * (Double.parseDouble(txtJuros.getText())/100));
                
                aprazo.setValor(valorV);
                
            }// TODO add your handling code here:
    }//GEN-LAST:event_btnInserirActionPerformed

