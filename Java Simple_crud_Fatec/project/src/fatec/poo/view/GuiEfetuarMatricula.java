    private void cboxCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboxCursoActionPerformed
        String selecionado;
        selecionado = (String)cboxCurso.getSelectedItem();
        int x;
        
        for (x = 0; x < cadastroC.size(); x++){
            if (cadastroC.get(x).getSigla().equals(selecionado)){
                break;
            }
        }
        
        txtValor.setText(String.valueOf(cadastroC.get(x).getValor()));
        
      
       
        
    }//GEN-LAST:event_cboxCursoActionPerformed

