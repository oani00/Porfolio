    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int x;
        for (x = 0; x < cadastroF.size(); x++){
            if (cadastroF.get(x).getRegistro() == Integer.parseInt(txtRegistro.getText())){
                if (cbxTipo.getSelectedIndex() == 0 && 
                    cadastroF.get(x) instanceof FuncionarioHorista){                       
                       break;
                }else
                  if (cbxTipo.getSelectedIndex() == 1 && 
                      cadastroF.get(x) instanceof FuncionarioMensalista){                      
                      break; 
                  }else 
                      if (cbxTipo.getSelectedIndex() == 2 && 
                          cadastroF.get(x) instanceof FuncionarioComissionado){
                          break; 
                  } 
            }
        }

        if (x < cadastroF.size()){
            posFunc = x; //localizou o objeto Funcionario (horista, mensalista ou comissionado) no ArrayList
        }else{
            posFunc = -1;//não localizou o objeto Projeto no ArrayList
        }

        if (posFunc >= 0){
            txtNome.setText(cadastroF.get(posFunc).getNome());
            txtDtAdmissao.setText(cadastroF.get(posFunc).getDtAdmissao());
            txtCargo.setText(cadastroF.get(posFunc).getCargo());
            if (cbxTipo.getSelectedIndex() == 0){
                txtValor.setText(String.valueOf(((FuncionarioHorista)cadastroF.get(posFunc)).getValHorTrab()));
            }else
               if (cbxTipo.getSelectedIndex() == 1){
                  txtValor.setText(String.valueOf(((FuncionarioMensalista)cadastroF.get(posFunc)).getValSalMin()));
               }else{                  
                  txtValor.setText(String.valueOf(((FuncionarioComissionado)cadastroF.get(posFunc)).getSalBase()));
                  txtTaxaComissao.setText(String.valueOf(((FuncionarioComissionado)cadastroF.get(posFunc)).getTaxaComissao()));
               } 
            
            //Atualizando o ComboBox cbxDepartamento de acordo
            //como o departamento associado
            cbxDepartamento.setSelectedItem(cadastroF.get(posFunc).getDepartamento().getNome());
            
            btnConsultar.setEnabled(false);
            btnInserir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }else{
            btnConsultar.setEnabled(false);
            btnInserir.setEnabled(true);
            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            txtNome.requestFocus();
        }
        cbxTipo.setEnabled(false);
        txtRegistro.setEnabled(false);
        txtNome.setEnabled(true);
        txtDtAdmissao.setEnabled(true);
        txtCargo.setEnabled(true);
        txtValor.setEnabled(true);
        cbxDepartamento.setEnabled(true);
        if (cbxTipo.getSelectedIndex() == 2){
            txtTaxaComissao.setEnabled(true);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
       //instanciação do objeto da classe Funcionario Horista, Mensalista ou Comissionado
       Funcionario func=null; 
       if (cbxTipo.getSelectedIndex() == 0){
           func = new FuncionarioHorista(Integer.parseInt(txtRegistro.getText()),
                                         txtNome.getText(), 
                                         txtDtAdmissao.getText(),
                                         Double.parseDouble(txtValor.getText())); 
       }else 
          if (cbxTipo.getSelectedIndex() == 1){
             func = new FuncionarioMensalista(Integer.parseInt(txtRegistro.getText()),
                                              txtNome.getText(), 
                                              txtDtAdmissao.getText(),
                                              Double.parseDouble(txtValor.getText()));           
        }else  
           if (cbxTipo.getSelectedIndex() == 2){
               func = new FuncionarioComissionado(Integer.parseInt(txtRegistro.getText()),
                                                  txtNome.getText(), 
                                                  txtDtAdmissao.getText(),
                                                  Double.parseDouble(txtTaxaComissao.getText()));            
               ((FuncionarioComissionado)func).setSalBase(Double.parseDouble(txtValor.getText()));
           }      
        func.setCargo(txtCargo.getText());       
       
        //Estabelecendo a associação binária entre um objeto Departamento
        //com um objeto Funcionario (Horista, Mensalista ou Comissionado)
        cadastroD.get(cbxDepartamento.getSelectedIndex()).addFuncionario(func);
        
        //insere o objeto no ArrayList
        cadastroF.add(func); 
        
        txtRegistro.setText(null);
        txtNome.setText(null);
        txtDtAdmissao.setText(null);
        txtCargo.setText(null);
        txtValor.setText(null);
        
        if (cbxTipo.getSelectedIndex() == 2){
           txtTaxaComissao.setText(null);
           txtTaxaComissao.setEnabled(false);
        }
        btnConsultar.setEnabled(true);
        btnInserir.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        cbxTipo.setEnabled(true);
        cbxDepartamento.setEnabled(false);
        txtRegistro.setEnabled(true);
        txtNome.setEnabled(false);
        txtDtAdmissao.setEnabled(false);
        txtCargo.setEnabled(false);
        txtValor.setEnabled(false);
        cbxTipo.setSelectedIndex(0);
        cbxDepartamento.setSelectedIndex(0);
        txtRegistro.requestFocus();
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        cbxTipo.setEnabled(false);
        txtRegistro.setEnabled(false);
        cadastroF.get(posFunc).setNome(txtNome.getText());
        cadastroF.get(posFunc).setDtAdmissao(txtDtAdmissao.getText());
        cadastroF.get(posFunc).setCargo(txtCargo.getText());
        
        //verifica se houve a alteração de departamento para o funcionário
        //caso positivo estabelece uma nova associação binária entre
        //Funcionario e Departamento
        if (!(cadastroF.get(posFunc).getDepartamento().getNome()).equals(cbxDepartamento.getSelectedItem())){
            //remove a atual associação binária
            cadastroF.get(posFunc).getDepartamento().removeFuncionario(cadastroF.get(posFunc));
            
            //estabelece a nova associaçã binária
            cadastroD.get(cbxDepartamento.getSelectedIndex()).addFuncionario(cadastroF.get(posFunc));
        }
        
        if (cbxTipo.getSelectedIndex() == 0){
                ((FuncionarioHorista)cadastroF.get(posFunc)).setValHorTrab(Double.parseDouble(txtValor.getText()));
            }else
               if (cbxTipo.getSelectedIndex() == 1){
               ((FuncionarioMensalista)cadastroF.get(posFunc)).setValSalMin(Double.parseDouble(txtValor.getText()));
               }else{
                  txtValor.setText(String.valueOf(((FuncionarioComissionado)cadastroF.get(posFunc)).getSalBase()));
                  ((FuncionarioComissionado)cadastroF.get(posFunc)).setTaxaComissao(Double.parseDouble(txtTaxaComissao.getText()));
               } 

        txtRegistro.setText(null);
        txtNome.setText(null);
        txtDtAdmissao.setText(null);
        txtCargo.setText(null);
        txtValor.setText(null);
        cbxTipo.setSelectedIndex(0);
        cbxDepartamento.setSelectedIndex(0);
        if (cbxTipo.getSelectedIndex() == 2){
           txtTaxaComissao.setText(null);
           txtTaxaComissao.setEnabled(false);
           txtTaxaComissao.setVisible(false);
           lblTaxaComissao.setVisible(false);
        }
        btnConsultar.setEnabled(true);
        btnInserir.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        txtRegistro.setEnabled(true);
        txtNome.setEnabled(false);
        txtDtAdmissao.setEnabled(false);
        txtCargo.setEnabled(false);
        txtValor.setEnabled(false);
        cbxTipo.setEnabled(true);
        cbxDepartamento.setEnabled(false);
        txtRegistro.requestFocus();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (posFunc >= 0){
            //remove a associação binária entre Funcionario e Departamento
            cadastroF.get(posFunc).getDepartamento().removeFuncionario(cadastroF.get(posFunc));
            cadastroF.remove(posFunc);
            posFunc = -1;
        }
        txtRegistro.setText(null);
        txtNome.setText(null);
        txtDtAdmissao.setText(null);
        txtCargo.setText(null);
        txtValor.setText(null);
        cbxTipo.setSelectedIndex(0);
        cbxDepartamento.setSelectedIndex(0);
        
        if (cbxTipo.getSelectedIndex() == 2){
           txtTaxaComissao.setText(null);
           txtTaxaComissao.setEnabled(false);
           txtTaxaComissao.setVisible(false);
           lblTaxaComissao.setVisible(false);
        }
        btnConsultar.setEnabled(true);
        btnInserir.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        cbxTipo.setEnabled(true);
        cbxDepartamento.setEnabled(false);
        txtRegistro.setEnabled(true);
        txtNome.setEnabled(false);
        txtDtAdmissao.setEnabled(false);
        txtCargo.setEnabled(false);
        txtValor.setEnabled(false);          
        txtRegistro.requestFocus();
    }//GEN-LAST:event_btnExcluirActionPerformed

