    private String[] listarDepartamentos(){
        String[] listaDeptos = new String[cadastroD.size()];
        
        for (int x=0; x < cadastroD.size(); x++){
            listaDeptos[x] = cadastroD.get(x).getNome();
        }
        return listaDeptos;
    }
